using System.Text;
using DwarfMapper.Generator.Model;

namespace DwarfMapper.Generator.Pipeline;

internal static class MapEmitter
{
    public static string Emit(MapperClassModel model)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// SPDX-License-Identifier: GPL-2.0-only");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(model.Namespace))
        {
            sb.Append("namespace ").Append(model.Namespace).AppendLine(";");
            sb.AppendLine();
        }

        sb.Append(model.Accessibility).Append(" partial class ").AppendLine(model.ClassName);
        sb.AppendLine("{");

        foreach (var method in model.Methods)
        {
            EmitMethod(sb, method, "    ");
        }

        foreach (var synth in model.SynthesizedMethods)
        {
            sb.AppendLine();
            sb.Append(synth.Code);
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitMethod(StringBuilder sb, MapMethodModel method, string indent)
    {
        sb.Append(indent).Append(method.Accessibility).Append(" partial ")
          .Append(method.ReturnTypeFullName).Append(' ').Append(method.MethodName)
          .Append('(').Append(method.ParameterTypeFullName).Append(' ').Append(method.ParameterName).AppendLine(")");
        sb.Append(indent).AppendLine("{");

        if (method.ParameterIsReferenceType)
        {
            sb.Append(indent).Append("    if (").Append(method.ParameterName)
              .Append(" is null) throw new global::System.ArgumentNullException(nameof(")
              .Append(method.ParameterName).AppendLine("));");
        }

        sb.Append(indent).Append("    return new ").AppendLine(method.ReturnTypeFullName);
        sb.Append(indent).AppendLine("    {");
        foreach (var member in method.Members)
        {
            sb.Append(indent).Append("        ").Append(member.TargetName).Append(" = ");
            if (member.ConverterMethod is null)
            {
                sb.Append(method.ParameterName).Append('.').Append(member.SourceName);
            }
            else
            {
                sb.Append(member.ConverterMethod).Append('(')
                  .Append(method.ParameterName).Append('.').Append(member.SourceName).Append(')');
            }
            sb.AppendLine(",");
        }
        sb.Append(indent).AppendLine("    };");
        sb.Append(indent).AppendLine("}");
    }
}
